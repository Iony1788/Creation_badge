package com.example.badge_app.model;



public class SignaturePhotoResponse {
	private String signatureImage;
	private String dateSignature;
	private String photoIdentite;
	private String name;
	public String getSignatureImage() {
		return signatureImage;
	}
	public void setSignatureImage(String signatureImage) {
		this.signatureImage = signatureImage;
	}
	public String getDateSignature() {
		return dateSignature;
	}
	public void setDateSignature(String dateSignature) {
		this.dateSignature = dateSignature;
	}
	public String getPhotoIdentite() {
		return photoIdentite;
	}
	public void setPhotoIdentite(String photoIdentite) {
		this.photoIdentite = photoIdentite;
	}
	public String getName() {
		return name;
	}
	public void setName(String nameWorker) {
		this.name = nameWorker;
	}
	
	
	
	
	
	
}
